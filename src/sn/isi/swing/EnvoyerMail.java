    private void btnEnvoieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnvoieActionPerformed
        // TODO add your handling code here:
        try {
            String idC = this.idC.getText();
            String corps = this.corps.getText();
            String objet = this.objet.getText();
            DB db = new DB();
            if (idC.trim().equals("") || corps.trim().equals("") || objet.trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "veuillez remplir tous champs !!!");
            } else {
                Mail m = new Mail();
                m.setObjet(objet);
                m.setCorps(corps);
                try {
                    int id = Integer.parseInt(idC);
                    m.getClient().setId(id);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Veuillez entrer un chiffre !!!");
                }

                db.open();
                String sql = "INSERT INTO mail VALUES (null, ?, ?, ?)";
                // initialisation de la requete
                db.init(sql);
                db.getPstm().setString(1, m.getObjet());
                db.getPstm().setString(2, m.getCorps());
                db.getPstm().setInt(3, m.getClient().getId());
                //execution de la requete Insert
                db.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Envoyer avec success !");

            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnEnvoieActionPerformed

