    private void btnModification2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModification2ActionPerformed
        // TODO add your handling code here:
        try {

            String prenom = this.prenomM.getText();
            String nom = this.nomM.getText();
            String email = this.mailM.getText();
            String tel = this.telM.getText();
            String vId = this.id.getText();
            //try{
            //  int id = Integer.parseInt(vId);

            if (prenom.trim().equals("") || nom.trim().equals("") || email.trim().equals("") || tel.trim().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Veuillez renseignez tous les champs");

            } else {
                DB db = new DB();
                Client c = new Client();
                c.setPrenom(prenom);
                c.setNom(nom);
                //controle du mail
                if(verificationMail(email) == true){
                    c.setEmail(email);
                }else{
                    JOptionPane.showMessageDialog(rootPane,"mail invalide !!!");
                }
                
               if(verificationTel(tel) == true){
                    c.setTel(tel);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "<html>numero de telephone invalide !!!<br>"
                            + "essayez le format 77 777 77 77");
                }
                try {
                    int id = Integer.parseInt(vId);
                    c.setId(id);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "veuillez entrer un chiffre");
                }
                //c.setId();

                db.open();
                String sql = "UPDATE client SET prenom = ?, nom = ?, email = ?, tel = ? WHERE id = ?";
                // initialisation de la requete
                db.init(sql);
                db.getPstm().setString(1, c.getPrenom());
                db.getPstm().setString(2, c.getNom());
                db.getPstm().setString(3, c.getEmail());
                db.getPstm().setString(4, c.getTel());
                db.getPstm().setInt(5, c.getId());
                //execution de la requete Insert
                int ok = db.executeUpdate();

                if (ok == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Modifier avec success !!!");
                    viderChamps();
                }

            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnModification2ActionPerformed

