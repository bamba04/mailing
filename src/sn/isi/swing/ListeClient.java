    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        // TODO add your handling code here:

        int selectedRow = tabClient.getSelectedRow();
        //recuperation de l'id
        String vid = tabClient.getValueAt(selectedRow, 0).toString();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(rootPane, "Veuillez selectionner une ligne");
        } else {
            int choix = JOptionPane.showConfirmDialog(rootPane,
                    "voulez supprimer le client",
                    "Confirmation",
                    JOptionPane.YES_NO_OPTION);
            if (choix == JOptionPane.YES_OPTION) {
                //recuperation de la ligne a supprime
                this.tab.removeRow(selectedRow);
                //recherche de l'id dans la base
                for (Client client : list) {

                    int idC = client.getId();
                    try {
                        int id = Integer.parseInt(vid);
                        //comparaison de l'id du tableau avec celui de la bdd
                        if (id == idC) {
                            //suppression du client dans le tableau
                            JOptionPane.showMessageDialog(rootPane, "nom : '" + client.getNom() + "'<br>email : '" + client.getEmail() + "'");

                            DB db = new DB();
                            //suppression du client dans la bdd
                            db.open();
                            String sql = "DELETE FROM client WHERE id = ?";
                            db.init(sql);
                            db.getPstm().setInt(1, id);
                            db.executeUpdate();
                            list.remove(client);
                        }
                    } catch (Exception e) {

                    }
                }

            }
        }
    }//GEN-LAST:event_supprimerActionPerformed

