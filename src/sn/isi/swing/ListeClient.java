    public ListeClient() {

        initComponents();
        try {
            DB db = new DB();
            //creation de la liste des clients
            this.list = new ArrayList<Client>();
            db.open();
            String sql = "SELECT * FROM client";
            db.init(sql);
            ResultSet rset = db.executeSelect();
            while (rset.next()) {
                Client c = new Client();
                c.setId(rset.getInt(1));
                c.setPrenom(rset.getString(2));
                c.setNom(rset.getString(3));
                c.setEmail(rset.getString(4));
                c.setTel(rset.getString(5));
                //ajout des clients de la bdd dans notre liste
                list.add(c);
            }
            //creation d'un tableau local
            this.tab = new DefaultTableModel();
            this.tab.addColumn("#");
            this.tab.addColumn("Prenom");
            this.tab.addColumn("Nom");
            this.tab.addColumn("Email");
            this.tab.addColumn("tel");

            for (Client client : list) {
                this.tab.addRow(new Object[]{client.getId(), client.getPrenom(), client.getNom(), client.getEmail(), client.getTel()});
            }
            //affectation du tableau au tableau de notre interface
            this.tabClient.setModel(tab);
        } catch (Exception e) {

        }

    }

